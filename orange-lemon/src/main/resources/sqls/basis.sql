CREATE TABLE BASIS_SUBSTANCE(
ID VARCHAR(64) PRIMARY KEY NOT NULL,
BASIS_SUBSTANCE_TYPE_ID VARCHAR(64),

DESCN VARCHAR(128),
STATUS VARCHAR(64),
CREATE_TIME TIMESTAMP,
MODIFY_TIME TIMESTAMP,
DISP_INX INT
);

CREATE TABLE BASIS_SUBSTANCE_TYPE(
ID VARCHAR(64) PRIMARY KEY NOT NULL,
TYPE_NAME VARCHAR(64),

DESCN VARCHAR(128),
STATUS VARCHAR(64),
CREATE_TIME TIMESTAMP,
MODIFY_TIME TIMESTAMP,
DISP_INX INT
);

CREATE TABLE BASIS_ATTR(
ID VARCHAR(64) PRIMARY KEY NOT NULL,
ATTR_NAME VARCHAR(64),
ATTR_COLUMN VARCHAR(64),
ATTR_TYPE VARCHAR(64),
REQUIRED VARCHAR(64),
CAN_SELECT VARCHAR(64),
VATTR_ID VARCHAR(64),
VCLS_ID VARCHAR(64),
VCOLUMN VARCHAR(64),
VFILTER_ID VARCHAR(64),

BASIS_SUBSTANCE_TYPE_ID VARCHAR(64),
DESCN VARCHAR(128),
STATUS VARCHAR(64),
CREATE_TIME TIMESTAMP,
MODIFY_TIME TIMESTAMP,
DISP_INX INT
);

CREATE TABLE BASIS_VALUE(
ID VARCHAR(64) PRIMARY KEY NOT NULL,
STRING_VALUE VARCHAR(64),
TEXT_VALUE TEXT,
