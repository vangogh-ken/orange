BASIS_ATTR_ID VARCHAR(64),
BASIS_SUBSTANCE_ID VARCHAR(64),

DESCN VARCHAR(128),
STATUS VARCHAR(64),
CREATE_TIME TIMESTAMP,
MODIFY_TIME TIMESTAMP,
DISP_INX INT
);

CREATE TABLE BASIS_STATUS(
